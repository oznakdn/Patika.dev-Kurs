using System;

namespace MathMetotlari
{
    class Program
    {
        static void Main(string[] args)
        {
                /**************Math******************
                1- Abs (Mutlak değer)
                2- Pow (üs alma)
                3- sqrt(karekök alma)
                4- Ceiling (Yukarı yuvarlar)
                5- Floor (Aşağı yuvarlar)
                6- Round (yakına yuvarlar)
                7- Max (en büyük sayı);
                8- Min (en küçük sayı);

                */

        Console.WriteLine(Math.Abs(-1)); // ==> 3
        Console.WriteLine(Math.Pow(5,2)); // ==> 25
        Console.WriteLine(Math.Sqrt(81)); // ==> 9

        Console.WriteLine(Math.Ceiling(3.50)); // ==> 4
        Console.WriteLine(Math.Floor(3.50)); // ==> 3
        Console.WriteLine(Math.Round(3.40)); // ==> 4

        Console.WriteLine(Math.Max(10,15)); // ==> 15
        Console.WriteLine(Math.Min(10,15)); // ==> 10
        
        
        






        }
    }
}
