            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = rng.Next(-20, 55),
                Summary = Summaries[rng.Next(Summaries.Length)]
            })
            .ToArray();
        }


        // Bu action metodu => api/WeatherForecasts?id=3    şeklinde  parametre verilerek çalışır.

        [HttpGet("{id}")]
        public IEnumerable<WeatherForecast> GetForeCast([FromRoute] string id)
        {
